using System.IO;
using PeNet.Asn1;
using Xunit;

namespace PeNet.Asn1_Test {
    public class Asn1BmpStringTests: BaseTest {

        private static readonly byte[] _etalon = { 0x1e, 0x3e, 0x04, 0x33, 0x00, 0x2e, 0x00, 0x20, 0x04, 0x18, 0x04, 0x36, 0x04, 0x35, 0x04, 0x32, 0x04, 0x41, 0x04, 0x3a, 0x00, 0x2c, 0x00, 0x20, 0x04, 0x3f, 0x04, 0x35, 0x04, 0x40, 0x00, 0x2e, 0x00, 0x20, 0x04, 0x21, 0x04, 0x35, 0x04, 0x32, 0x04, 0x35, 0x04, 0x40, 0x04, 0x3d, 0x04, 0x4b, 0x04, 0x39, 0x00, 0x2c, 0x00, 0x20, 0x04, 0x34, 0x00, 0x2e, 0x00, 0x20, 0x00, 0x36, 0x00, 0x31 };

        [Fact]
        public void ReadTest() {
            var node = Asn1Node.ReadNode(new MemoryStream(_etalon));
            var typed = node as Asn1BmpString;
            Assert.NotNull(typed);
            Assert.Equal("г. Ижевск, пер. Северный, д. 61", typed.Value);
        }

        [Fact]
        public void WriteTest() {
            var node = new Asn1BmpString { Value = "г. Ижевск, пер. Северный, д. 61" };
            var data = node.GetBytes();
            AreEqual(_etalon, data);
        }
    }
}
